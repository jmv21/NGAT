namespace NGAT.Geo
{
    /// <summary>
    /// Represents the (amount) of a coordinate ordinates
    /// </summary>
    public enum OrdinatesKind
    {
        /// <summary>
        /// Represents an X ordinate
        /// </summary>
        X = 1,
        /// <summary>
        /// Represents X-Y ordinates
        /// </summary>
        XY = 2,
        /// <summary>
        /// Represents X-Y-Z ordinates
        /// </summary>
        XYZ = 3,
        /// <summary>
        /// Represents X-Y-Z and M ordinates
        /// </summary>
        XYZM = 4,
        /// <summary>
        /// Represents N ordinates
        /// </summary>
        N = -1
    }


}
